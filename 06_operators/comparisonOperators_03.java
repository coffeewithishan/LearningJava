public class comparisonOperators_03 {
    public static void main(String[] args) {
        int a = 121;
        int b = 111;
    System.out.println(a > b); // true
    System.out.println(a >= b); // true
    System.out.println(a < b); // false
    System.out.println(a <= b); // false
    System.out.println(a == b); // false
    System.out.println(a = b); // 111 (it gets assigned the value of b)
    System.out.println(a != b); // true

}
    }